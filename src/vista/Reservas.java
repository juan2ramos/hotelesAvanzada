    private void llenarHoteles(Ciudad ciudad) {
        
        List hoteles = HotelMgr.getInstance().getHotelesPorCiudad(ciudad);
        HotelesCB.removeAllItems();
        for (int i = 0; i < hoteles.size(); i++) {
            HotelesCB.addItem( hoteles.get(i));
        }
    }

    private void llenarCiudades() {
        List ciudades = CiudadMgr.getInstance().getCiudades();
        CuidadCB.removeAllItems();
        for (int i = 0; i < ciudades.size(); i++) {
            CuidadCB.addItem( ciudades.get(i));
        }
        llenarHoteles((Ciudad) CuidadCB.getSelectedItem());
        //llenarHabitaciones();
        
    }
    private void llenarHabitaciones() {
       Hotel hotel = (Hotel) HotelesCB.getSelectedItem();
       if (hotel != null){
          List<Habitacion> hoteles = hotel.getHabitaciones();
       habitacionCB.removeAllItems();
        for (int i = 0; i < hoteles.size(); i++) {
            habitacionCB.addItem( hoteles.get(i));
        } 
       }
        
        
    }
